/**
 * Post-cap-sync fix for onesignal-cordova-plugin SPM compatibility.
 *
 * Capacitor 8 uses Swift Package Manager (SPM) for iOS plugins, but
 * onesignal-cordova-plugin is a Cordova plugin with no SPM support.
 * cap sync deletes any pre-existing sources and cannot auto-generate a
 * Package.swift for this plugin.  This script runs immediately after
 * cap sync and reconstructs the required SPM package wrapper so that
 * Xcode can resolve the dependency and compile the Objective-C bridge.
 *
 * Track: https://github.com/OneSignal/OneSignal-Cordova-SDK/issues/1069
 * Remove this script once OneSignal ships native SPM support.
 */

import { mkdirSync, copyFileSync, writeFileSync, existsSync } from "fs";
import { join, dirname } from "path";
import { fileURLToPath } from "url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = join(__dirname, "..");

const SRC_DIR = join(
  ROOT,
  "node_modules/onesignal-cordova-plugin/src/ios"
);
const DEST_DIR = join(
  ROOT,
  "ios/capacitor-cordova-ios-plugins/sources/OnesignalCordovaPlugin"
);

// Read the Capacitor SPM version from CapApp-SPM/Package.swift so we
// always stay in sync with the rest of the project.
import { readFileSync } from "fs";
const capPackageSwift = readFileSync(
  join(ROOT, "ios/App/CapApp-SPM/Package.swift"),
  "utf8"
);
const versionMatch = capPackageSwift.match(
  /capacitor-swift-pm\.git",\s*exact:\s*"([^"]+)"/
);
const CAP_SPM_VERSION = versionMatch ? versionMatch[1] : "8.2.0";

const ONE_SIGNAL_IOS_SDK_VERSION = "5.4.2";

const PACKAGE_SWIFT = `\
// swift-tools-version: 5.9
import PackageDescription

// Auto-generated by scripts/fix-onesignal-spm.mjs — DO NOT EDIT MANUALLY.
// This file is recreated after every cap sync because onesignal-cordova-plugin
// does not yet ship its own SPM package (see OneSignal-Cordova-SDK #1069).
let package = Package(
    name: "OnesignalCordovaPlugin",
    platforms: [.iOS(.v15)],
    products: [
        .library(
            name: "OnesignalCordovaPlugin",
            targets: ["OnesignalCordovaPlugin"])
    ],
    dependencies: [
        .package(url: "https://github.com/ionic-team/capacitor-swift-pm.git", exact: "${CAP_SPM_VERSION}"),
        .package(url: "https://github.com/OneSignal/OneSignal-iOS-SDK", exact: "${ONE_SIGNAL_IOS_SDK_VERSION}")
    ],
    targets: [
        .target(
            name: "OnesignalCordovaPlugin",
            dependencies: [
                .product(name: "Cordova", package: "capacitor-swift-pm"),
                .product(name: "OneSignalFramework", package: "OneSignal-iOS-SDK")
            ],
            path: ".",
            sources: ["OneSignalPush.m"],
            publicHeadersPath: ".",
            cSettings: [
                .headerSearchPath(".")
            ]
        )
    ]
)
`;

if (!existsSync(SRC_DIR)) {
  console.error(
    `[fix-onesignal-spm] ERROR: source dir not found: ${SRC_DIR}`
  );
  process.exit(1);
}

mkdirSync(DEST_DIR, { recursive: true });
copyFileSync(join(SRC_DIR, "OneSignalPush.h"), join(DEST_DIR, "OneSignalPush.h"));
copyFileSync(join(SRC_DIR, "OneSignalPush.m"), join(DEST_DIR, "OneSignalPush.m"));
writeFileSync(join(DEST_DIR, "Package.swift"), PACKAGE_SWIFT, "utf8");

console.log(
  `[fix-onesignal-spm] ✔ OnesignalCordovaPlugin SPM package written to:\n  ${DEST_DIR}`
);
